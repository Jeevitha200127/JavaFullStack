public class DataTypes {
    public static void main(String[] args) {
        boolean b=true;
        System.out.println(b);
        char c='M';
        System.out.println(c);
        byte b1=126;
        System.out.println(b1);
        int i=44;
        System.out.println(i);
        long l=1234;
        System.out.println(l);
        double d=0.8d;
        System.out.println(d);
        float f=0.3f;
        System.out.println(f);


    }
}
